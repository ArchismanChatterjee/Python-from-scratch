import math
n= 91
m= 45
print (math.sqrt(n))
# Finds out the square root of n
print (math.sqrt(m))
# Finds out the square root of n
round (math.sqrt(n))
# In case of decimals, just rounds it off. 
print (math.sqrt(n)+ math.sqrt(m))
# Adds the square root of two numbers
round (math.sqrt(n)+ math.sqrt(m))
# Rounds the addition of the square root of two numbers
print(math.ceil(math.sqrt(n)+ math.sqrt(m)))
# Returns the highest integer value
print(math.floor(math.sqrt(n)+ math.sqrt(m)))
# Returns the lowest integer value. 
print(math.log10(math.sqrt(n)+ math.sqrt(m)))
